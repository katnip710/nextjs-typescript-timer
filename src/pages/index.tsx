import Head from "next/head";
import { useEffect, useState } from "react";
import Christmas from "../components/Christmas";
import Halloween from "../components/Halloween";

function Home() {

    const [findHoliday, setFindHoliday] = useState(false)
    const [days, setDays] = useState(0)
    const [hours, setHours] = useState(0)
    const [minutes, setMinutes] = useState(0)
    const [seconds, setSeconds] = useState(0)

    useEffect(() =>{
        const target = new Date('10/31/2022 23:59:59')
        //const target = new Date ('08/12/2022 1:51:00')

        const interval = setInterval(() => {
        const now = new Date()
        const difference = target.getTime() - now.getTime()

        const d = Math.floor(difference / (1000 * 60 * 60 * 24))
        setDays(d)

        const h = Math.floor(
            (difference % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60)
        )
        setHours(h)

        const m = Math.floor(
            (difference % (1000 * 60 * 60)) / (1000 * 60)
        )
        setMinutes(m)

        const s = Math.floor(
            (difference % (1000 * 60)) / 1000
        )
        setSeconds(s)

        if(d <= 0 && h <= 0 && m <= 0 && s <= m){
            setFindHoliday(true)
        }

        }, 1000)

        return () => clearInterval(interval)
    }, [])

  return (
    <>
      <Head>
        <title>CountDown Timer</title>
        <meta name="Countdown Timer" content="Generated by create-t3-app. Created by Kat Lea" />
      </Head>

      {findHoliday ? (

        <Christmas />

      ) : (

        <Halloween />

      )}
    </>
  );
};

export default Home;

